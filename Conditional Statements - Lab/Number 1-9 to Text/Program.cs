    {
        static void Main(string[] args)
        {
            int num = int.Parse(Console.ReadLine());

            if (num == 1)
            {
                Console.Write("one");
            }
            if (num == 2)
            {
                Console.Write("two");
            }
            if (num == 3)
            {
                Console.Write("three");
            }
            if (num == 4)
            {
                Console.Write("four");
            }
            if (num == 5)
            {
                Console.Write("five");
            }
            if (num == 6)
            {
                Console.Write("six");
            }
            if (num == 7)
            {
                Console.Write("seven");
            }
            if (num == 8)
            {
                Console.Write("eight");
            }
            if (num == 9)
            {
                Console.Write("nine");
            }
            if (num >= 10)
            {
                Console.Write("number too big");
            }

        }
    }
}
